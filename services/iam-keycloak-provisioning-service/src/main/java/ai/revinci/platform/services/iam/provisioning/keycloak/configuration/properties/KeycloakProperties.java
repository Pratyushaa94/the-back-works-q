package ai.revinci.platform.services.iam.provisioning.keycloak.configuration.properties;

import org.springframework.boot.context.properties.ConfigurationProperties;

import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

@Getter
@Setter
@NoArgsConstructor
@ConfigurationProperties(prefix = "revinciai.platform.iam.keycloak")
public class KeycloakProperties {
    /** The URL of the Keycloak server. */
    private String serverUrl;

    /** The realm to which the client belongs. */
    private String realm;

    /** The client ID of the client. */
    private String clientId;

    /** The grant type to be used for authentication. */
    private String grantType;

    /** The username of the client. */
    private String username;

    /** The password of the client. */
    private String password;

    /** Application Url. */
    private String applicationUrl;
}
